package Self;

public class Loops {
    public static void main(String[] args) {
        // Loops

        // 1 - For Loops
        for(int i = 0; i<=100; i++){
            System.out.println(i);
        }
        
        // while loops
        int j = 100;
        while(j>=1){
            System.out.println(j);
            j--;
        }

        //Do - While loop
        int k = 1;
        do{
            System.out.println(k);
            k++;
        }while(k<=100);

    }
    
}
