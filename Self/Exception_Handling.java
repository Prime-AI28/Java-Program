package Self;

public class Exception_Handling {
    public static void main(String[] args) {
        // Exception Handling
        // TRY
        // CATCH

        int[] marks = {97,98,95};
        try{
            System.out.println(marks[5]);

        }catch(Exception exception){
            // do Something after Catching
            System.out.println();
        }
        System.out.println("I am Aman");
    }
    
}
